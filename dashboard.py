@app.route("/api/generate", methods=["POST"])
def api_generate():
    """
    Generate a response for a question using Claude.
    Body: { "question": "...", "course": "COGS 9" | "CHEM 11" }
    Returns: { "answer": "..." }
    """
    data     = request.get_json() or {}
    question = (data.get("question") or "").strip()
    course   = (data.get("course") or "").strip()

    if not question:
        return jsonify({"error": "question is required"}), 400

    try:
        client = anthropic.Anthropic()

        if course == "CHEM 11":
            system = _CHEM11_GENERATE_SYSTEM
        else:
            syllabus_path = BASE_DIR / os.environ.get("SYLLABUS_PATH", "syllabus.txt")
            try:
                syllabus_text    = syllabus_path.read_text(encoding="utf-8").strip()
                syllabus_section = f"SYLLABUS:\n{syllabus_text}"
            except Exception:
                syllabus_section = ""
            system = _COGS9_GENERATE_SYSTEM.format(syllabus_section=syllabus_section)

        answer = ""
        violations = []
        for attempt in range(3):
            resp = client.messages.create(
                model="claude-haiku-4-5",
                max_tokens=400,
                system=system,
                messages=[{"role": "user", "content": question}],
            )
            answer = "".join(b.text for b in resp.content if b.type == "text").strip()
            if course == "CHEM 11":
                violations = _check_answer(answer)
                if not violations:
                    break
            else:
                break

        payload = {"answer": answer}
        if violations:
            payload["warnings"] = violations
        return jsonify(payload)

    except Exception as e:
        return jsonify({"error": str(e)}), 500


@app.route("/api/send", methods=["POST"])
def api_send():
    """
    Post a manually reviewed answer to Piazza.
    Body: { "cid": "...", "answer": "...", "course": "COGS 9" | "CHEM 11" }
    Returns: { "ok": true, "type": "s_answer" } or { "error": "..." }
    """
    data   = request.get_json() or {}
    cid    = (data.get("cid") or "").strip()
    answer = (data.get("answer") or "").strip()
    course = (data.get("course") or "").strip()

    if not cid or not answer:
        return jsonify({"error": "cid and answer are required"}), 400

    try:
        networks = _get_piazza_networks()
        network  = networks.get(course)
        if not network:
            return jsonify({"error": f"No Piazza network configured for: {course}"}), 400

        network._rpc.content_student_answer(cid, answer, revision=0)
        return jsonify({"ok": True, "type": "s_answer"})

    except RuntimeError as e:
        return jsonify({"error": str(e)}), 503
    except Exception as e:
        return jsonify({"error": f"Piazza post failed: {e}"}), 500


# ── All-posts cache ───────────────────────────────────────────────────────────

_POSTS_CACHE_PATH = BASE_DIR / "posts_cache.json"
_POSTS_CACHE_TTL  = 300  # seconds


def _strip_html(raw):
    return _html.unescape(re.sub(r"<[^>]+>", " ", raw or "")).strip()


def _is_posts_cache_fresh():
    if not _POSTS_CACHE_PATH.exists():
        return False
    cache_mtime = _POSTS_CACHE_PATH.stat().st_mtime
    if (time.time() - cache_mtime) >= _POSTS_CACHE_TTL:
        return False
    # Invalidate if scan_log changed after the cache was written
    scan_log_path = BASE_DIR / "scan_log.json"
    if scan_log_path.exists() and scan_log_path.stat().st_mtime > cache_mtime:
        return False
    return True


def _build_all_posts(limit=500):
    """Fetch all posts from Piazza, merged with scan_log gate results."""
    networks = _get_piazza_networks()

    scan_log = _read_json_file(BASE_DIR / "scan_log.json") or []
    if not isinstance(scan_log, list):
        scan_log = []
    scan_map = {
        (e.get("course", ""), str(e.get("nr", ""))): e
        for e in scan_log
    }

    posts = []
    for course, network in networks.items():
        for post in network.iter_all_posts(limit=limit):
            if post.get("type") != "question":
                continue
            nr  = post.get("nr", 0)
            cid = post.get("id", "")
            hist = (post.get("history") or [{}])[0]
            question = (
                _strip_html(hist.get("subject", "")) + "\n" +
                _strip_html(hist.get("content", ""))
            ).strip()

            has_answer = any(
                c.get("type") in ("i_answer", "s_answer")
                for c in post.get("children", [])
            )

            s          = scan_map.get((course, str(nr)), {})
            bot_posted = bool(s.get("posted"))
            posts.append({
                "nr":         nr,
                "cid":        cid,
                "course":     course,
                "question":   question[:400],
                "has_answer": has_answer,
                "bot_posted": bot_posted,
                "posted":     bot_posted,
                "category":   "bot" if bot_posted else (s.get("category") or ("answered" if has_answer else "")),
                "gate_answer": s.get("gate_answer") or "",
                "source":     s.get("source") or "",
                "time":       s.get("time") or "",
            })

    posts.sort(key=lambda x: x.get("nr") or 0, reverse=True)
    return posts


@app.route("/api/all_posts")
def api_all_posts():
    """Return all Piazza posts (questions), merged with scan_log. 5-min cache."""
    force = request.args.get("refresh") == "1"

    if not force and _is_posts_cache_fresh():
        cached = _read_json_file(_POSTS_CACHE_PATH)
        if cached and isinstance(cached.get("posts"), list):
            return jsonify({
                "posts":      cached["posts"],
                "fetched_at": cached.get("fetched_at"),
                "from_cache": True,
            })

    try:
        posts   = _build_all_posts()
        payload = {
            "posts":      posts,
            "fetched_at": time.strftime("%Y-%m-%d %H:%M:%S"),
            "from_cache": False,
        }
        try:
            _POSTS_CACHE_PATH.write_text(json.dumps(payload))
        except Exception:
            pass
        return jsonify(payload)

    except RuntimeError as e:
        return jsonify({"error": str(e)}), 503
    except Exception as e:
        cached = _read_json_file(_POSTS_CACHE_PATH)
        if cached and isinstance(cached.get("posts"), list):
            return jsonify({**cached, "from_cache": True, "stale": True})
        return jsonify({"error": str(e)}), 500


