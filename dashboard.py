# ── Answer quality checklist (CHEM 11 only) ──────────────────────────────────

_CHECKLIST = [
    (r"[—–]",                                                   "em/en dash"),
    (r";",                                                       "semicolon"),
    (r"(?m)^\s*[-•*]\s",                                        "bullet point"),
    (r"(?m)^\s*\d+\.\s",                                        "numbered list"),
    (r"\*\*|__|\*[^*\s]|_[^_\s]",                              "markdown formatting"),
    (r"(?i)^(hi|hello|hey|dear|greetings)[,!\s]",              "greeting at start"),
    (r"(?i)(hope this helps|let me know|feel free|any questions|good luck|don't hesitate)", "sign-off phrase"),
    (r"(?i)(as an ai|i'm an ai|language model|i'm claude|ai assistant)", "AI self-identification"),
    (r"(?i)(great question|excellent question|good question|that's a great)", "sycophantic opener"),
    (r"(?i)(furthermore|it is important to note|it's important to note|in conclusion|in summary)", "AI filler phrase"),
    (r"(?i)(you should|you need to|make sure you|remember to|always remember|don't forget|i recommend|i suggest|my advice|pro tip)", "advice/tip"),
]

_MIN_WORDS = 30
_MAX_WORDS = 250

def _check_answer(text):
    failures = []
    for pattern, msg in _CHECKLIST:
        if re.search(pattern, text):
            failures.append(msg)
    words = len(text.split())
    if words < _MIN_WORDS:
        failures.append(f"too short ({words} words)")
    if words > _MAX_WORDS:
        failures.append(f"too long ({words} words)")
    return failures


