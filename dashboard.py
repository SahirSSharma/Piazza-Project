# ── Piazza lazy connection ────────────────────────────────────────────────────

_piazza_networks = None  # dict: {"COGS 9": network, "CHEM 11": network}


def _get_piazza_networks():
    global _piazza_networks
    if _piazza_networks is not None:
        return _piazza_networks

    email    = os.environ.get("PIAZZA_EMAIL", "")
    password = os.environ.get("PIAZZA_PASSWORD", "")
    if not email or not password:
        raise RuntimeError("PIAZZA_EMAIL / PIAZZA_PASSWORD not configured in environment")

    p = Piazza()
    p.user_login(email=email, password=password)

    networks = {}
    cogs9_id  = os.environ.get("PIAZZA_NETWORK") or os.environ.get("PIAZZA_NETWORK_COGS9")
    chem11_id = os.environ.get("CHEM_PIAZZA_NETWORK") or os.environ.get("PIAZZA_NETWORK_CHEM11")
    if cogs9_id:
        networks["COGS 9"] = p.network(cogs9_id)
    if chem11_id:
        networks["CHEM 11"] = p.network(chem11_id)

    _piazza_networks = networks
    return networks


# ── Helpers ───────────────────────────────────────────────────────────────────

def _read_json_file(path):
    """Read a JSON file, return None if missing or corrupt."""
    try:
        return json.loads(Path(path).read_text())
    except Exception:
        return None


def _is_pid_alive(pid):
    """Check if a process is alive via os.kill(pid, 0)."""
    if pid is None:
        return False
    try:
        os.kill(int(pid), 0)
        return True
    except ProcessLookupError:
        return False
    except PermissionError:
        return True  # exists but owned by another user
    except Exception:
        return False


